public class VariableDataType {
    public static void main(String[] args) {
        int a = 5;
        float b = 5.5f;
        double c = 5.5;
        boolean d = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
